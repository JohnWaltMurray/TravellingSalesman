
public class SalesmanTableEntry {

	private int value;
	private int vertex;
	
	public SalesmanTableEntry(int val, int ver) {
		setValue(val);
		setVertex(ver);
	}

	public int getVertex() {
		return vertex;
	}

	public void setVertex(int vertex) {
		this.vertex = vertex;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}
	
	
}
